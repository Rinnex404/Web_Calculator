function getAdd(num1, num2){
    let result = num1 + num2;
    return result;
}

function getSubtract(num1, num2){
    let result = num1 - num2;
    return result;
}

function getMultiply(num1, num2){
    let result = num1 * num2;
    return result;
}

function getDivide(num1, num2){
    let result = num1 / num2;
    return result;
}

